CREATE TABLE player
(
    player_id VARCHAR(300) PRIMARY KEY,
    birth_year VARCHAR(300),
    birth_month VARCHAR(300),
    birth_day VARCHAR(300),
    birth_country VARCHAR(300),
    birth_state VARCHAR(300),
    birth_city VARCHAR(300),
    death_year VARCHAR(300),
    death_month VARCHAR(300),
    death_day VARCHAR(300),
    death_country VARCHAR(300),
    death_state VARCHAR(300),
    death_city VARCHAR(300),
    name_first VARCHAR(300),
    name_last VARCHAR(300),
    name_given VARCHAR(300),
    weight VARCHAR(300),
    height VARCHAR(300),
    bats VARCHAR(300),
    throws VARCHAR(300),
    debut VARCHAR(300),
    final_game VARCHAR(300),
    retro_id VARCHAR(300),
    bbref_id VARCHAR(300)
);